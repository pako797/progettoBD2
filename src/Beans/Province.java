package Beans;

public class Province {
	
	private String _id;
	private String sigla;
	
	
	public Province() {
		super();
	}


	public String get_id() {
		return _id;
	}


	public void set_id(String _id) {
		this._id = _id;
	}


	public String getSigla() {
		return sigla;
	}


	public void setSigla(String sigla) {
		this.sigla = sigla;
	}
	
	

}
